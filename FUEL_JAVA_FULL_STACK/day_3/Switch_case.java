public class Switch_case {
    public static void main(String[] args) {
        int number = 20;
        switch (number) {
            case 10:System.out.println("10");
                break;
        
            case 20:System.out.println("20");
                break;
            case 30:
                System.out.println("30");
                break;
                default:System.out.println("Number is not between 10 and 30");
        }
        
    }
}
